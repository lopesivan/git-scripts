#!/usr/bin/env bash

test -n "$DEBUG" && set -x

# ----------------------------------------------------------------------------
# Modo strict
set -euo pipefail
# ----------------------------------------------------------------------------

##############################################################################
##############################################################################
##############################################################################

# ----------------------------------------------------------------------------
# Run!
for f in $(git status --porcelain | grep '^??' | sed 's/^?? //'); do
    test -L "$f" && echo $f   >> .gitignore;
    test -d "$f" && echo $f\* >> .gitignore;
done

# ----------------------------------------------------------------------------
exit 0
